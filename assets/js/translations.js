/**
 * Helper: Get a Bengali translation for a given English string.
 * Falls back to the original English string if no translation is found.
 *
 * @param {string} key - The English string to translate
 * @param {string} [lang='bn'] - The language code (default: 'bn')
 * @returns {string} The translated string, or the original if not found
 */
function thanchiTranslate(key, lang) {
    lang = lang || 'bn';
    if (THANCHI_TRANSLATIONS[lang] && THANCHI_TRANSLATIONS[lang][key]) {
        return THANCHI_TRANSLATIONS[lang][key];
    }
    return key;
}

/**
 * Helper: Translate a string with sprintf-style replacements.
 * Supports %s, %d, %1$s, %2$s style placeholders.
 *
 * @param {string} key - The English string to translate (with placeholders)
 * @param {...*} args - Values to replace placeholders
 * @returns {string} The translated and formatted string
 */
function thanchiTranslatef(key) {
    var translated = thanchiTranslate(key);
    var args = Array.prototype.slice.call(arguments, 1);
    var argIndex = 0;

    return translated.replace(/%(\d+\$)?[sd]/g, function(match) {
        // Handle positional args like %1$s, %2$s
        var posMatch = match.match(/^%(\d+)\$/);
        if (posMatch) {
            var pos = parseInt(posMatch[1], 10) - 1;
            return args[pos] !== undefined ? args[pos] : match;
        }
        // Handle simple %s, %d
        return args[argIndex] !== undefined ? args[argIndex++] : match;
    });
}

